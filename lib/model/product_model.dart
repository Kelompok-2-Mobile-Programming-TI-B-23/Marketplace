class Product {
  final String id; // Menyimpan ID produk
  final String name;
  final String description;
  final String image;
  final String category;
  final String storeName;
  final double price;
  final String rating;

  Product({
    required this.id,
    required this.name,
    required this.description,
    required this.image,
    required this.category,
    required this.storeName,
    required this.price,
    required this.rating,
  });
}
